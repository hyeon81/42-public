#include "ft_printf.h"

void	ft_putnbr(int n, int *res)
{
	char	c;

	if (n >= 0)
	{
		if (n > 9)
		{
			c = n % 10 + '0';
			ft_putnbr(n / 10, res);
			write (1, &c, 1);
			(*res)++;
		}
		else
		{
			c = n % 10 + '0';
			write (1, &c, 1);
			(*res)++;
		}
	}
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*res += 11;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		(*res)++;
		ft_putnbr(-n, res);
	}
}
