#include "ft_printf.h"

void	ft_print_ui(unsigned int n, int* res)
{
	char	c;

	if (n > 9)
	{
		c = n % 10 + '0';
		ft_print_ui(n / 10, res);
		write (1, &c, 1);
		(*res)++;
	}
	else
	{
		c = n % 10 + '0';
		write (1, &c, 1);
		(*res)++;
	}
}
