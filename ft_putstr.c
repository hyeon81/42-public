#include "ft_printf.h"

void	ft_putstr(char *s, int	*res)
{
	int	i;

	i = 0;
	if (s == 0)
	{
		write(1, "(null)", 6);
		*res += 6;
		return ;
	}
	while (s[i] != 0)
	{
		write(1, &s[i], 1);
		i++;
		(*res)++;
	}
}
